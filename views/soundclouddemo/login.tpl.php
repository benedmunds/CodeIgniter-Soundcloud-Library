<h2>What is this?</h2>

<p>This is a basic demo of the CodeIgniter SoundCloud PHP API Library which lets you login to your SoundCloud account using <a href="http://oauth.net/" rel="external">OAuth</a>.</p>
<p>Once logged in the user can view his profile and upload a new track to his account. The track will be kept private by default.</p>
<p>Note that this demo neither save your login credentials or account information.</p>
<p>This demo and library has been developed by <a href="http://twitter.com/benedmunds" rel="external">Ben Edmunds</a> and <a href="http://plasticated.com" rel="external">Plasticated</a> based on the class written by <a href="http://qvister.se" rel="external">Anton Lindqvist</a></p>

